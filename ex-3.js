// Exercise #3: Celsius to Fahrenheit
/* celsius เป็น Variable แบบ let ที่เก็บ Value ของค่าอุณหภูมิในหน่วย Celsius เป็น 30
ให้เขียน Expression ในการแปลงอุณหภูมิในหน่วย Celsius เป็นหน่วย Fahrenheit
โดยสูตรการหาอุณหภูมิในหน่วยฟาเรนไฮต์คือ (celsius x 1.8) + 32
จากนั้นเก็บผลลัพธ์เข้าไปในตัว Variable ที่มีชื่อว่า fahrenheit */

let celsius = 30;
let fahrenheit = (celsius * 1.8) + 32;
console.log(fahrenheit);